#include <stdio.h>
#define HEIGHT 100
#define NUMBER 3.14
#define LETTER 'A'
#define LETTER_SEQUENCE "ABC"
#define BACKSLASH_CHAR "\\"
int main()
{
    printf("Height is: %d \n", HEIGHT);
    printf("Value of pi is: %f \n", NUMBER);
    printf("Letter is: %c \n", LETTER);
    printf("Sequence is: %s \n", LETTER_SEQUENCE);
    printf("Back Slash is: %c \n", BACKSLASH_CHAR);
    return 0;
}