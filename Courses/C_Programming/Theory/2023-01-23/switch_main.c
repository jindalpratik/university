#include <stdio.h>
int main()
{
    int a = 2;
    switch(a)
    {
        case 1:
            printf("The value is 1\n");
            break;
        case 2:
            printf("The value is 2\n");
            break;
        case 3:
            printf("The value is 3\n");
            break;
        default:
            printf("Value is 7\n");
            break;
    }
    return 0;
}