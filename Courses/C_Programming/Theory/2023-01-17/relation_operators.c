#include <stdio.h>
int main()
{
    int x = 10, y = 15;
    printf("x = %d\n", x);
    printf("y = %d\n\n", y);
    printf("x > y : %d\n", x > y); 
    printf("x >= y : %d\n", x >= y);
    printf("x < y : %d\n", x < y);
    printf("x <= y : %d\n", x <= y);
    printf("x == y : %d\n", x == y);
    printf("x != y : %d\n", x != y);
    return 0;
}